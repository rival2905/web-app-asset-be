        <h1>Syarat dan Ketentuan Penggunaan Aplikasi Absensi THL</h1>

        <h2>1. Pendahuluan</h2>
        <p>Aplikasi <strong>Absensi THL</strong> adalah aplikasi internal yang digunakan oleh <strong>Dinas Bina Marga
                dan Penataan Ruang Provinsi Jawa Barat</strong> untuk mencatat kehadiran Tenaga Harian Lepas (THL).
            Dengan menggunakan aplikasi ini, pengguna setuju untuk mematuhi syarat dan ketentuan yang berlaku.</p>

        <h2>2. Hak Akses dan Pengguna</h2>
        <p>Aplikasi ini hanya dapat digunakan oleh pegawai yang telah terdaftar oleh <strong>Dinas Bina Marga dan
                Penataan Ruang Provinsi Jawa Barat</strong>.</p>
        <ul>
            <li><strong>Pendaftaran tidak tersedia untuk umum</strong>. Hanya akun yang diberikan oleh administrator
                yang dapat mengakses aplikasi ini.</li>
            <li>Setiap pengguna bertanggung jawab atas penggunaan akunnya dan tidak diperbolehkan membagikan informasi
                akun kepada pihak lain.</li>
        </ul>

        <h2>3. Izin Akses Perangkat</h2>
        <p>Aplikasi <strong>Absensi THL</strong> memerlukan beberapa izin akses pada perangkat pengguna untuk dapat
            berfungsi dengan baik:</p>
        <ul>
            <li><strong>INTERNET</strong>: Digunakan untuk mengirim dan menerima data absensi.</li>
            <li><strong>FOREGROUND_SERVICE</strong>: Digunakan untuk memastikan layanan absensi tetap berjalan di latar
                depan.</li>
            <li><strong>ACCESS_FINE_LOCATION</strong>: Digunakan untuk mendapatkan lokasi presisi tinggi guna memastikan
                absensi dilakukan di lokasi yang sesuai.</li>
            <li><strong>ACCESS_COARSE_LOCATION</strong>: Digunakan untuk mendapatkan perkiraan lokasi jika GPS tidak
                tersedia.</li>
        </ul>

        <h2>4. Batasan Penggunaan</h2>
        <ul>
            <li>Aplikasi ini hanya boleh digunakan untuk keperluan absensi resmi oleh <strong>Dinas Bina Marga dan
                    Penataan Ruang Provinsi Jawa Barat</strong>.</li>
            <li>Pengguna dilarang mencoba memodifikasi, menduplikasi, atau menyalahgunakan aplikasi untuk kepentingan
                pribadi atau pihak lain.</li>
            <li>Pengguna dilarang menggunakan perangkat lain atau metode yang tidak sah untuk mengelabui sistem absensi.
            </li>
        </ul>

        <h2>5. Pengolahan Data</h2>
        <ul>
            <li>Data yang dikumpulkan dari aplikasi ini akan digunakan hanya untuk keperluan absensi dan administrasi
                pegawai.</li>
            <li>Data tidak akan disebarkan kepada pihak ketiga tanpa izin resmi dari <strong>Dinas Bina Marga dan
                    Penataan Ruang Provinsi Jawa Barat</strong>.</li>
            <li>Semua informasi pribadi pengguna akan dijaga kerahasiaannya sesuai dengan kebijakan privasi yang
